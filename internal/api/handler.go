func (h *Handler) ListDevices(w http.ResponseWriter, r *http.Request) {
	instances := h.manager.ListInstances()

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(instances)
}

// RenameDevice renames an existing instance
// @Summary Rename a device
// @Description Updates the name of an existing device/instance
// @Tags device
// @Accept json
// @Produce json
// @Param id path string true "Device ID"
// @Param request body CreateDeviceRequest true "New Device Name"
// @Success 200 {object} map[string]string
// @Router /device/{id}/rename [put]
func (h *Handler) RenameDevice(w http.ResponseWriter, r *http.Request) {
	deviceID := chi.URLParam(r, "id")
	var req CreateDeviceRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, "Invalid request body", http.StatusBadRequest)
		return
	}

	if req.Name == "" {
		http.Error(w, "Name is required", http.StatusBadRequest)
		return
	}

	err := h.manager.RenameInstance(deviceID, req.Name)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(map[string]string{"status": "success", "device_id": deviceID, "name": req.Name})
}

// GetQR returns the QR code stream for a device
// @Summary Get QR code stream
// @Description Connects to SSE stream to receive QR codes for pairing
// @Tags device
// @Param id path string true "Device ID"
// @Produce text/event-stream
// @Router /device/{id}/qr [get]
func (h *Handler) GetQR(w http.ResponseWriter, r *http.Request) {
	deviceID := chi.URLParam(r, "id")
	logger.Info("SSE connection initiated", zap.String("device", deviceID))

	// Pass the request context so we know when client disconnects
	qrChan, err := h.manager.GetQR(r.Context(), deviceID)
	if err != nil {
		logger.Error("Failed to get QR channel", zap.Error(err))
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	// Server Sent Events (SSE) for QR updates
	w.Header().Set("Content-Type", "text/event-stream")
	w.Header().Set("Cache-Control", "no-cache")
	w.Header().Set("Connection", "keep-alive")
	// CORS headers just in case, though usually handled by middleware
	w.Header().Set("Access-Control-Allow-Origin", "*")

	flusher, ok := w.(http.Flusher)
	if !ok {
		http.Error(w, "Streaming unsupported!", http.StatusInternalServerError)
		return
	}

	// Send initial ping or comment to establish connection immediately
	fmt.Fprintf(w, ": connected\n\n")
	flusher.Flush()

	for {
		select {
		case <-r.Context().Done():
			logger.Info("SSE client disconnected", zap.String("device", deviceID))
			return
		case code, ok := <-qrChan:
			if !ok {
				logger.Info("QR channel closed", zap.String("device", deviceID))
				return
			}
			if code == "" {
				continue
			}
			logger.Info("Sending QR Code event to client", zap.String("device", deviceID))
			// Using "data: <code_content>\n\n" format
			_, err := fmt.Fprintf(w, "data: %s\n\n", code)
			if err != nil {
				logger.Error("Failed to write to ResponseWriter", zap.Error(err))
				return
			}
			flusher.Flush()
		}
	}
}

// GetStatus checks the connection status of a device
// @Summary Get device status
// @Description Returns the connection status of the device
// @Tags device
// @Param id path string true "Device ID (JID)"
// @Produce json
// @Success 200 {object} map[string]string
// @Router /device/{id}/status [get]
func (h *Handler) GetStatus(w http.ResponseWriter, r *http.Request) {
	deviceID := chi.URLParam(r, "id")
	// Try direct map access first (if using JID format from URL)
	client := h.manager.GetClient(deviceID)

	if client == nil {
		// Fallback or error
		http.Error(w, "Device not found", http.StatusNotFound)
		return
	}

	status := "disconnected"
	if client.IsConnected() {
		status = "connected"
	}

	json.NewEncoder(w).Encode(map[string]string{"status": status})
}

// SendInteractiveButton sends a WhatsApp interactive button message via Queue
// @Summary Send an interactive button message
// @Description Sends an interactive button message (Queued)
// @Tags message
// @Accept json
// @Produce json
// @Param request body SendMessageRequest true "Message Request"
// @Success 200 {object} map[string]string
// @Router /message/interactive/button [post]
func (h *Handler) SendInteractiveButton(w http.ResponseWriter, r *http.Request) {
	var req SendMessageRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, "Invalid request body", http.StatusBadRequest)
		return
	}

	payloadBytes, err := json.Marshal(req)
	if err != nil {
		http.Error(w, "Failed to encode message to queue", http.StatusInternalServerError)
		return
	}

	err = h.rmqClient.Publish(r.Context(), "send_message_queue", payloadBytes)
	if err != nil {
		http.Error(w, "Failed to enqueue message", http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(map[string]string{"status": "queued"})
}

