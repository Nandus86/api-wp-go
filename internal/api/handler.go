type Handler struct {
	manager   *whatsapp.MultiClientManager
	rmqClient *rabbitmq.Client
}

func NewHandler(manager *whatsapp.MultiClientManager, rmqClient *rabbitmq.Client) *Handler {
	return &Handler{
		manager:   manager,
		rmqClient: rmqClient,
	}
}

func (h *Handler) Router() http.Handler {
	r := chi.NewRouter()
	r.Use(middleware.Logger)

	r.Post("/device", h.CreateDevice)
	r.Get("/device", h.ListDevices)
	r.Put("/device/{id}/rename", h.RenameDevice)
	r.Get("/device/{id}/qr", h.GetQR)
	r.Get("/device/{id}/status", h.GetStatus)
	r.Post("/message/send", h.SendText)
	r.Post("/message/interactive/button", h.SendInteractiveButton)
	r.Post("/message/interactive/copy", h.SendInteractiveCopyButton)
	r.Post("/message/media", h.SendMedia)

	r.Get("/docs/*", httpSwagger.Handler(
		httpSwagger.URL("/docs/doc.json"), //The url pointing to API definition
	))

	return r
}

type CreateDeviceRequest struct {
	Name string `json:"name"`
}

// CreateDevice creates a new WhatsApp device session
// @Summary Create a new device
// @Description Generates a new device ID and initializes a client
// @Tags device
// @Accept json
// @Produce json
// @Param request body CreateDeviceRequest true "Device Name"
// @Success 200 {object} map[string]string
// @Router /device [post]
func (h *Handler) CreateDevice(w http.ResponseWriter, r *http.Request) {
	var req CreateDeviceRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, "Invalid request body", http.StatusBadRequest)
		return
	}

	if req.Name == "" {
		req.Name = "Instance-" + time.Now().Format("150405")
	}

	deviceID, _, err := h.manager.NewClientWithName(req.Name)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(map[string]string{
		"device_id": deviceID,
		"name":      req.Name,
	})
}

// ListDevices returns a list of all connected devices with their details
// @Summary List devices
