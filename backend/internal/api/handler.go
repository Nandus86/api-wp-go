// GetQR returns the QR code stream for a device
// @Summary Get QR code stream
// @Description Connects to SSE stream to receive QR codes for pairing
// @Tags device
// @Param id path string true "Device ID"
// @Produce text/event-stream
// @Router /device/{id}/qr [get]
func (h *Handler) GetQR(w http.ResponseWriter, r *http.Request) {
	deviceID := chi.URLParam(r, "id")
	logger.Info("SSE connection initiated", zap.String("device", deviceID))

	// Pass the request context so we know when client disconnects
	qrChan, err := h.manager.GetQR(r.Context(), deviceID)
	if err != nil {
		logger.Error("Failed to get QR channel", zap.Error(err))
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	// Server Sent Events (SSE) for QR updates
	w.Header().Set("Content-Type", "text/event-stream")
	w.Header().Set("Cache-Control", "no-cache")
	w.Header().Set("Connection", "keep-alive")
	// CORS headers just in case, though usually handled by middleware
	w.Header().Set("Access-Control-Allow-Origin", "*")

	flusher, ok := w.(http.Flusher)
	if !ok {
		http.Error(w, "Streaming unsupported!", http.StatusInternalServerError)
		return
	}

	// Send initial ping or comment to establish connection immediately
	fmt.Fprintf(w, ": connected\n\n")
	flusher.Flush()

	// Keep-alive ticker to prevent Traefik/Nginx from dropping idle connections
	ticker := time.NewTicker(5 * time.Second)
	defer ticker.Stop()

	for {
		select {
		case <-r.Context().Done():
			logger.Info("SSE client disconnected", zap.String("device", deviceID))
			return
		case <-ticker.C:
			// Send a comment ping to keep connection alive
			_, err := fmt.Fprintf(w, ": ping\n\n")
			if err != nil {
				return
			}
			flusher.Flush()
		case code, ok := <-qrChan:
			if !ok {
				logger.Info("QR channel closed", zap.String("device", deviceID))
				return
			}
			if code == "" {
				continue
			}
			logger.Info("Sending QR Code event to client", zap.String("device", deviceID))
			// Using "data: <code_content>\n\n" format
			_, err := fmt.Fprintf(w, "data: %s\n\n", code)
			if err != nil {
				logger.Error("Failed to write to ResponseWriter", zap.Error(err))
				return
			}
			flusher.Flush()
		}
	}
}

