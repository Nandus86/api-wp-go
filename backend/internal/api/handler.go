// GetStatus returns the current connection status of a device
// @Summary Get device status
// @Description Returns the connection status of the device
// @Tags device
// @Param id path string true "Device ID (JID)"
// @Produce json
// @Success 200 {object} map[string]string
// @Router /device/{id}/status [get]
func (h *Handler) GetStatus(w http.ResponseWriter, r *http.Request) {
	deviceID := chi.URLParam(r, "id")
	// Try direct map access first (if using JID format from URL)
	client := h.manager.GetClient(deviceID)

	if client == nil {
		// Fallback or error
		http.Error(w, "Device not found", http.StatusNotFound)
		return
	}

	status := "disconnected"
	if client.IsLoggedIn() {
		status = "connected"
	}

	json.NewEncoder(w).Encode(map[string]string{"status": status})
}

// SendInteractiveButton sends a WhatsApp interactive button message via Queue
// @Summary Send an interactive button message
// @Description Sends an interactive button message (Queued)
// @Tags message
// @Accept json
// @Produce json
// @Param request body SendMessageRequest true "Message Request"
// @Success 200 {object} map[string]string
// @Router /message/interactive/button [post]
func (h *Handler) SendInteractiveButton(w http.ResponseWriter, r *http.Request) {
	var req SendMessageRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, "Invalid request body", http.StatusBadRequest)
		return
	}

	payloadBytes, err := json.Marshal(req)
	if err != nil {
		http.Error(w, "Failed to encode message to queue", http.StatusInternalServerError)
		return
	}

	err = h.rmqClient.Publish(r.Context(), "send_message_queue", payloadBytes)
	if err != nil {
		http.Error(w, "Failed to enqueue message", http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(map[string]string{"status": "queued"})
}

